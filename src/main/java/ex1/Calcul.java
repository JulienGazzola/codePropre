package ex1;

/** Réalise un calcul */
public class Calcul {

	/** Retourne le résultat de l'addition de 2 nombres */
	public int get1(int nb1, int nb2) {
		return nb1 + nb2;
	}

	/** Retourne le résultat de la soustraction de 2 nombres */
	public int get2(int nb1, int nb2) {
		return nb1 - nb2;
	}
}
